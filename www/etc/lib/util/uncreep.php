<?

// get all defined vars in this scope - excluding super-globals
// --------------------------------------------------------------------------------------
   $cvl = array_keys((array)get_creep(get_defined_vars()));
// --------------------------------------------------------------------------------------


// loop through all defined vars and unset each
// --------------------------------------------------------------------------------------
   for ($i=0; $i < sizeOf($cvl); $i++)
   { unset(${str_replace('$','',$cvl[$i])}); }
// --------------------------------------------------------------------------------------


// finally, unset the vars defined here
// --------------------------------------------------------------------------------------
   unset($cvl,$i);
// --------------------------------------------------------------------------------------

?>
