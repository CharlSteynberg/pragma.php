<?

// def :: make.cfg - extends: `devl`
// --------------------------------------------------------------------------------------
   set::{'devl.make.cfg'}
   (
   // fnc :: cfg - implements: `devl::make.cfg()`
   // -----------------------------------------------------------------------------------
      function($src,$inf)
      {
      // cnd :: skip - if file is _ini & home-dir contains items
      // --------------------------------------------------------------------------------
         if ($inf->base === '_ini.cfg.jso')
         {
            $hdi = path::info($inf->home);

            if ($hdi->stat === 200)
            { return false; }
         }
      // --------------------------------------------------------------------------------

      // rsl :: write - updated `$src`
      // --------------------------------------------------------------------------------
         path::make($inf->path,$src);
      // --------------------------------------------------------------------------------
      }
   // -----------------------------------------------------------------------------------
   );
// --------------------------------------------------------------------------------------

?>
