   set::{'devl.make.fnc'}
   (
   // fnc :: fnc - implements: `devl::make.fnc()`
   // -----------------------------------------------------------------------------------
      function($src,$inf)
      {
      // def :: local - vars
      // --------------------------------------------------------------------------------
