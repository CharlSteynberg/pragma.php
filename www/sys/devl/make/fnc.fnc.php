      // rsl :: return - updated `$src`
      // --------------------------------------------------------------------------------
         return str($src)->swop
         (
            ['tplRef','parent','tplMap','tplFnc'],
            [$ref, $cls, "$cls.$ref", $fnc]
         );
      // --------------------------------------------------------------------------------
      }
   // -----------------------------------------------------------------------------------
   );
